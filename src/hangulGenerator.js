function generateSyllable(initial, vowel, final = "") {
  const i = CHO.indexOf(initial);
  const j = JUNG.indexOf(vowel);
  const k = JONG.indexOf(final);

  if (i === -1 || j === -1 || k === -1) return null;

  const code = 0xac00 + i * 21 * 28 + j * 28 + k;
  return String.fromCharCode(code);
}

export function generateSyllableData(consonants, vowels) {
  const result = [];

  consonants.forEach((c) => {
    vowels.forEach((v) => {
      const char = generateSyllable(c.char, v.char);

      if (!char) return;

      result.push({
        char,
        romaji: c.romaji.split("/")[0] + v.romaji, // simple combine
        components: [c.char, v.char],
        group: `cv-${c.char}`, // dynamic group
      });
    });
  });

  return result;
}
